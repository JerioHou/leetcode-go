package leetcode_go

type MyListNode struct {
	Val  int
	Next *MyListNode
}

func removeNthFromEnd(head *MyListNode, n int) *MyListNode {
	// 哨兵节点
	sentinel := &MyListNode{0, head}
	fast, slow := head, sentinel

	for i := 0; i < n; i++ {
		fast = fast.Next
	}
	for fast != nil {
		fast = fast.Next
		slow = slow.Next
	}
	slow.Next = slow.Next.Next
	return sentinel.Next
}
